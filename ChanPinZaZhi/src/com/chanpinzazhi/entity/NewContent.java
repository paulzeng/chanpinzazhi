package com.chanpinzazhi.entity;

public class NewContent {
	private String id;
	private String name;
	private String updateTime;
	private String navigateId;

	public NewContent() {
	}

	public NewContent(String id, String name, String updateTime,
			String navigateId) {
		this.id = id;
		this.name = name;
		this.updateTime = updateTime;
		this.navigateId = navigateId;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}

	public String getNavigateId() {
		return navigateId;
	}

	public void setNavigateId(String navigateId) {
		this.navigateId = navigateId;
	}

}
